# Adição
2 + 2 # => 4

# Subtração
3 - 2 # => 1

# Multiplicação
3 * 2 # => 6

# Divisão
20 / 10 # => 2

# Potenciação
2 ** 2  # => 4
5 ** 2  # => 25

# Modulo (resto da divisão)
12 % 3 # => 0 (8 / 2 = 4; não retorna resto)
10 % 4 # => 2 (10 / 4 = 2,5; logo retorna 2)

15 / 2.0 # => 7.5

15 / 2 # => 7, não 7.5

# Aspas duplas
'Hello, World'

# Aspas simples
'Meu nome é Oliver'

# Caracteres de fuga
"O 'Homem' é livre !"
'Somos ousados e \'Livres\' '

'Bom dia'[0] # => "B"

'Bom dia'[0..2] # => "Bom"

'Bom dia'[0, 4] # => "Bom d"

'Bom dia'[-1]  # => "a"


"Hello" == 'Hello'  # => true

"Good" == "Bom"  # => false

